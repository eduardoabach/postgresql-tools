				<br>[(3) Força do heroi] Uma vez por jogo durante ato heroico criar força para traspor amarras, algemas, portas de madeira, grandes pesos.
				<br>[(4) Confiança nos justos] Outras pessoas tem confiança nas suas decisões, com uma relação de respeito, mesmo os problemáticos. Não pode iniciar o jogo com esta pontuação, deve conquistar o direito com o narrador. -3 na dificuldade de teste de liderança e influência.
