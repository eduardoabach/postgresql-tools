				<br>[(2) Burlar trancas] Com uso de ferramentas diversas consegue abrir mais facilmente trancas e cadeados, -2 na dificuldade.
				<br>[(3) Arte da fuga] Sua experiência em lidar com carcere lhe concede facilidade de escapatória, -3 na dificuldade de testes referente a fuga.
				<br>[(4) Roubo de veiculos] Você tem pratica em abrir e ligar veiculos sem precisar da respectiva chave. -2 na dificuldade nos testes referentes.
				<br>[(5) Perito] Sua habilidade com o mundo do crime é única, -4 nos testes de fuga e identificar outros ladrões, -3 no roubo de carros e abrir trancas, possibilita abrir cofres e ganha -2 na dificuldade deste teste.
